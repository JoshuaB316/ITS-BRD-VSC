#ifndef OPERATIONS_H
#define OPERATIONS_H


/*
 ****************************************************************************************
 *  @brief      This function returns the latest value of the Stack
 *
 *  @return     void 
 ****************************************************************************************/
void p();

/*
 ****************************************************************************************
 *  @brief      This function returns all values of the Stack
 *
 *  @return     void 
 ****************************************************************************************/
void P();

/*
 ****************************************************************************************
 *  @brief      This function clears the Stack
 *
 *  @return     void 
 ****************************************************************************************/
void C();

/*
 ****************************************************************************************
 *  @brief      This function duplicates the latest value of the Stack
 *
 *  @return     void 
 ****************************************************************************************/
void d();

/*
 ****************************************************************************************
 *  @brief      This function swaps the latest two values of the Stack
 *
 *  @return     void 
 ****************************************************************************************/
void r();


#endif